numbers = [3, 1, 4, 1, 5, 9, 2]

'''
# numbers = [3, 1, 4, 1, 5, 9, 2]

# 1. numbers[0] -> 3
#    The first number in the list.

# 2. numbers[-1] -> 2
#    The last number in the list.

# 3. numbers[3] -> 1
#    The fourth number in the list.

# 4. numbers[:-1] -> [3, 1, 4, 1, 5, 9]
#    All numbers except the last one.

# 5. numbers[3:4] -> [1]
#    Just the fourth number, but in a list.

# 6. 5 in numbers -> True
#    Yes, 5 is in the list.

# 7. 7 in numbers -> False
#    No, 7 is not in the list.

# 8. "3" in numbers -> False
#    "3" is a string, but the list has numbers.

# 9. numbers + [6, 5, 3] -> [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
#    Adds the new numbers to the list.
'''

numbers[0] = "ten"
numbers[-1] = 1
print(numbers[2:])
print(9 in numbers)